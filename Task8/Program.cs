// Напишите программу, которая на вход принимает натуральное число N, 
// а на выходе показывает его цифры через запятую.

Console.WriteLine("Введите натуральное число: ");
int N = Convert.ToInt32(Console.ReadLine());

Console.Write("Цифры числа через запятую: ");

// Переменная для хранения текущей цифры числа
int digit;

// Способ получения цифр числа без использования цикла for
if (N > 0)
{
    while (N > 0)
    {
        // Получаем последнюю цифру числа
        digit = N % 10;

        // Выводим текущую цифру
        Console.Write(digit);

        // Проверяем, нужно ли выводить запятую
        if (N >= 10)
        {
            Console.Write(", ");
        }

        // Убираем последнюю цифру числа
        N = N / 10;
    }
    Console.WriteLine(); // Переходим на новую строку для красоты вывода
}
else
{
    Console.WriteLine("Ошибка ввода!");
}